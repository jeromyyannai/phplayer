package com.az.pplayer.Models;

public class VideoItem {
    public String Title;
    public String Video;
    public String Preview;
    public String VideoId;
    public String Image;

    public VideoItem(String title, String video, String preview, String videoId, String image) {
        Title = title;
        Video = video;
        Preview = preview;
        VideoId = videoId;
        Image = image;
    }
}
